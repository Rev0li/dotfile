  echo "‚úîÔ∏è Suppression des anciennes configurations termin√©e."
}

# Function to install Oh My Zsh
install_oh_my_zsh() {
  if [ ! -d "$HOME/.oh-my-zsh" ]; then
    sh -c "$(curl -fsSL https://raw.github.com/ohmyzsh/ohmyzsh/master/tools/install.sh)"
    log+=("‚úîÔ∏è Oh My Zsh a √©t√© install√©.")
  else
    log+=("‚úîÔ∏è Oh My Zsh est d√©j√† install√©.")
  fi
}

# Function to install Zsh plugins
install_zsh_plugins() {
  ZSH_CUSTOM=${ZSH_CUSTOM:-~/.oh-my-zsh/custom}
  git clone https://github.com/zsh-users/zsh-autosuggestions $ZSH_CUSTOM/plugins/zsh-autosuggestions 2>/dev/null || true
  git clone https://github.com/zsh-users/zsh-syntax-highlighting.git $ZSH_CUSTOM/plugins/zsh-syntax-highlighting 2>/dev/null || true
  log+=("‚úîÔ∏è Plugins Zsh install√©s (zsh-autosuggestions, zsh-syntax-highlighting).")
}

# Function to configure Neovim
configure_nvim() {
  SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
  NVIM_DIR="$SCRIPT_DIR/../nvim/nvim-linux64"
  NVIM_BIN="$NVIM_DIR/bin/nvim"
  
  if [ ! -f "$NVIM_BIN" ]; then
    log+=("‚ùå Neovim personnalis√© n'est pas trouv√© dans $NVIM_BIN. V√©rifiez l'installation.")
    exit 1
  fi
  
  export PATH="$NVIM_DIR/bin:$PATH"
  echo "export PATH=\"$NVIM_DIR/bin:\$PATH\"" >> "$HOME/.zshrc"
  log+=("‚úîÔ∏è Neovim personnalis√© ajout√© au PATH.")
  
  if [ ! -f "$HOME/.local/share/nvim/site/autoload/plug.vim" ]; then
    curl -fLo ~/.local/share/nvim/site/autoload/plug.vim --create-dirs \
      https://raw.githubusercontent.com/junegunn/vim-plug/master/plug.vim
    log+=("‚úîÔ∏è vim-plug a √©t√© install√© pour Neovim.")
  fi
  "$NVIM_BIN" +PlugInstall +qall
  log+=("‚úîÔ∏è Plugins Neovim install√©s.")
}

install_nerd_font() {
  FONT_NAME="FiraCode"
  FONT_URL="https://github.com/ryanoasis/nerd-fonts/releases/download/v2.3.3/FiraCode.zip"
  FONT_DIR="$HOME/.local/share/fonts"

  echo "T√©l√©chargement de la police Nerd Font ($FONT_NAME)..."
  mkdir -p "$FONT_DIR"
  curl -Lo "$FONT_DIR/$FONT_NAME.zip" "$FONT_URL"

  echo "Extraction des fichiers de police..."
  unzip -o "$FONT_DIR/$FONT_NAME.zip" -d "$FONT_DIR" > /dev/null
  rm "$FONT_DIR/$FONT_NAME.zip"

  echo "Mise √† jour du cache des polices utilisateur..."
  fc-cache -fv "$FONT_DIR" > /dev/null
  log+=("‚úîÔ∏è Police $FONT_NAME Nerd Font install√©e.")
}


# Function to install Kitty
install_kitty() {
  if command -v kitty &> /dev/null; then
    log+=("‚úîÔ∏è Kitty est d√©j√† install√©.")
    return
  fi

  log+=("üîÑ Installation de Kitty...")

  if [[ "$(uname -s)" == "Linux" ]]; then
    if command -v apt &> /dev/null; then
      sudo apt update && sudo apt install -y kitty
    elif command -v pacman &> /dev/null; then
      sudo pacman -S --noconfirm kitty
    elif command -v dnf &> /dev/null; then
      sudo dnf install -y kitty
    elif command -v zypper &> /dev/null; then
      sudo zypper install -y kitty
    else
      log+=("‚ùå Gestionnaire de paquets non pris en charge. Installez Kitty manuellement.")
      return
    fi
  elif [[ "$(uname -s)" == "Darwin" ]]; then
    if command -v brew &> /dev/null; then
      brew install kitty
    else
      log+=("‚ùå Homebrew non install√©. Installez Homebrew pour continuer.")
      return
    fi
  else
    log+=("‚ùå Syst√®me non pris en charge pour l'installation automatique de Kitty.")
    return
  fi

  if command -v kitty &> /dev/null; then
    log+=("‚úîÔ∏è Kitty install√© avec succ√®s.")
  else
    log+=("‚ùå L'installation de Kitty a √©chou√©.")
  fi
}

# Function to symlink files
symlink_files() {
  SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
  DOTFILES_DIR="$SCRIPT_DIR"
  
  ln -sf "$DOTFILES_DIR/../zsh/.zshrc" "$HOME/.zshrc"
  log+=("‚úîÔ∏è Fichier ~/.zshrc li√©.")

  mkdir -p "$HOME/.config"
  ln -sf "$DOTFILES_DIR/../nvim" "$HOME/.config/nvim"
  log+=("‚úîÔ∏è Configuration Neovim li√©e.")

  # Lier la config Kitty depuis ton Dotfile
  mkdir -p "$HOME/.config/kitty"
  ln -sf "$DOTFILES_DIR/../terminal/kitty.conf" "$HOME/.config/kitty/kitty.conf"
  log+=("‚úîÔ∏è Configuration Kitty li√©e.")

}

# Run the uninstall first
remove_old_configs

# Run the installation steps
install_oh_my_zsh
install_zsh_plugins
install_nerd_font
configure_nvim
symlink_files

# Display the log at the end
echo
echo "==============================="
echo " R√âCAPITULATIF DE L'INSTALLATION"
